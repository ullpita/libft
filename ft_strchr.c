#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	tmpc;

	tmpc = c;
	while (*s != tmpc)
	{
		if (*s == '\0')
			return (NULL);
		s++;
	}
	return ((char *)s);
}
